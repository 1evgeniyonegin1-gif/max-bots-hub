CONTENT_STYLE_INSTRUCTIONS = """
## ПРАВИЛА КАЧЕСТВЕННОГО КОНТЕНТА

### АНТИПАТТЕРНЫ (НЕ ДЕЛАЙ ТАК):
- Списки с галочками (выглядит как реклама из 2015)
- Фейковые цифры без источника ("87% людей...")
- Шаблонные вступления ("В современном мире...")
- Обязательный blockquote в каждом посте
- Избыток эмодзи (максимум 2-3 на пост)
- Призывы "подписывайтесь!" в каждом посте

### ПРАВИЛА ЖИВОГО КОНТЕНТА:
- Начинай с сомнения или неудачи ("Месяц назад не верил...")
- Конкретные детали из жизни ("Вчера клиент спросил...")
- Диалоги оживляют текст (Клиент: "..." Я: "...")
- Один продукт/тема = одна история
- Пиши как говоришь, не как пишут в учебниках

### ЖИВОЙ ЯЗЫК:
- Используй разговорные выражения
- Избегай канцеляризмов
- Не бойся коротких предложений
- Абзацы по 1-3 предложения максимум
"""

