def get_content_system_prompt(
    persona: str = "friend",
    business_context: str = "",
    brand_voice: str = ""
) -> str:
    """
    Получить системный промпт для генерации контента.

    Args:
        persona: Стиль общения (expert, friend, storyteller, mentor, energizer)
        business_context: Описание бизнеса/ниши клиента
        brand_voice: Особенности голоса бренда

    Returns:
        Системный промпт для AI
    """
    persona_prompt = PERSONA_PROMPTS.get(persona, PERSONA_PROMPTS["friend"])

    system_prompt = f"""Ты — контент-создатель для социальных сетей.

{persona_prompt}

{CONTENT_STYLE_INSTRUCTIONS}

"""

    if business_context:
        system_prompt += f"""
## КОНТЕКСТ БИЗНЕСА:
{business_context}

"""

    if brand_voice:
        system_prompt += f"""
## ГОЛОС БРЕНДА:
{brand_voice}

"""

    system_prompt += """
## ВАЖНО:
- Пиши на русском языке
- Длина поста: 150-300 слов
- Не используй хештеги в тексте
- Отвечай ТОЛЬКО текстом поста, без пояснений
"""

    return system_prompt


def get_post_generation_prompt(
    post_type: str,
    topic: str = "",
    additional_context: str = ""
) -> str:
    """
    Получить промпт для генерации конкретного поста.

    Args:
        post_type: Тип поста (product, motivation, tips, etc.)
        topic: Конкретная тема поста
        additional_context: Дополнительный контекст

    Returns:
        User prompt для AI
    """
    type_prompt = POST_TYPE_PROMPTS.get(post_type, POST_TYPE_PROMPTS["tips"])

    user_prompt = f"""{type_prompt}

"""

    if topic:
        user_prompt += f"""
## ТЕМА ПОСТА:
{topic}

"""

    if additional_context:
        user_prompt += f"""
## ДОПОЛНИТЕЛЬНЫЙ КОНТЕКСТ:
{additional_context}

"""

    user_prompt += """
Напиши пост по указанной структуре. Только текст поста, без комментариев.
"""

    return user_prompt


